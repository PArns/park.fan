function buildCoaster(
  { track, plain, lit }: BuildCtx,
  opts?: { pts?: THREE.Vector3[]; color?: number; speed?: number }
): Animated {
  const group = new THREE.Group();
  const pts = opts?.pts ?? [
    new THREE.Vector3(-34, 1, 24),
    new THREE.Vector3(-40, 1.5, 2),
    new THREE.Vector3(-32, 9, -16),
    new THREE.Vector3(-12, 13, -24),
    new THREE.Vector3(10, 7, -22),
    new THREE.Vector3(24, 3, -6),
    new THREE.Vector3(36, 5, 12),
    new THREE.Vector3(28, 2, 28),
    new THREE.Vector3(2, 1.2, 32),
    new THREE.Vector3(-18, 2, 30),
  ];
  const railColor = opts?.color ?? C.coaster;
  const trainSpeed = opts?.speed ?? 0.05;
  const curve = new THREE.CatmullRomCurve3(pts, true, 'catmullrom', 0.5);
  const SEG = 360;
  const frames = curve.computeFrenetFrames(SEG, true);
  const railOffset = 0.55;
  const leftPts: THREE.Vector3[] = [];
  const rightPts: THREE.Vector3[] = [];
  const centers: THREE.Vector3[] = [];
  for (let i = 0; i <= SEG; i++) {
    const p = curve.getPointAt(i / SEG);
    centers.push(p);
    const side = frames.binormals[i].clone().multiplyScalar(railOffset);
    leftPts.push(p.clone().add(side));
    rightPts.push(p.clone().sub(side));
  }
  const railMat = lit({ color: railColor, metalness: 0.2, roughness: 0.4 }, 0.7);
  const makeRail = (points: THREE.Vector3[]) => {
    const c = new THREE.CatmullRomCurve3(points, true);
    const mesh = new THREE.Mesh(track.geo(new THREE.TubeGeometry(c, SEG, 0.16, 8, true)), railMat);
    mesh.castShadow = true;
    return mesh;
  };
  group.add(makeRail(leftPts));
  group.add(makeRail(rightPts));

  const tieMat = lit({ color: 0xffe08a, roughness: 0.5 }, 0.4);
  const tieGeo = track.geo(new THREE.BoxGeometry(railOffset * 2 + 0.2, 0.1, 0.18));
  for (let i = 0; i < SEG; i += 5) {
    const tie = new THREE.Mesh(tieGeo, tieMat);
    tie.position.copy(centers[i]);
    const m = new THREE.Matrix4();
    m.lookAt(new THREE.Vector3(0, 0, 0), curve.getTangentAt(i / SEG), frames.normals[i]);
    tie.quaternion.setFromRotationMatrix(m);
    group.add(tie);
  }
  const postMat = plain({ color: 0x8a5a2b, metalness: 0, roughness: 0.85 });
  for (let i = 0; i < SEG; i += 14) {
    const p = centers[i];
    if (p.y < 1.2) continue;
    const post = new THREE.Mesh(track.geo(new THREE.CylinderGeometry(0.18, 0.22, p.y, 8)), postMat);
    post.position.set(p.x, p.y / 2, p.z);
    post.castShadow = true;
    group.add(post);
  }

  const train = new THREE.Group();
  group.add(train);
  const cars: THREE.Group[] = [];
  const carBodyGeo = track.geo(new THREE.BoxGeometry(0.9, 0.6, 1.3));
  const headGeo = track.geo(new THREE.SphereGeometry(0.22, 10, 10));
  const headMat = plain({ color: 0xffe0bd, roughness: 0.7 });
  for (let i = 0; i < 5; i++) {
    const car = new THREE.Group();
    const carMat = lit({ color: RIDE_COLORS[i % RIDE_COLORS.length], roughness: 0.4 }, 0.8);
    const body = new THREE.Mesh(carBodyGeo, carMat);
    body.position.y = 0.35;
    body.castShadow = true;
    car.add(body);
    for (const hx of [-0.22, 0.22]) {
      const head = new THREE.Mesh(headGeo, headMat);
      head.position.set(hx, 0.78, -0.1);
      car.add(head);
    }
    train.add(car);
    cars.push(car);
  }
  const up = new THREE.Vector3(0, 1, 0);
  const tmpT = new THREE.Vector3();
  const tmpM = new THREE.Matrix4();
  const place = (car: THREE.Group, t: number) => {
    const tt = ((t % 1) + 1) % 1;
    car.position.copy(curve.getPointAt(tt));
    curve.getTangentAt(tt, tmpT);
    car.position.y += 0.32;
    tmpM.lookAt(new THREE.Vector3(0, 0, 0), tmpT, up);
    car.quaternion.setFromRotationMatrix(tmpM);
  };

  return {
    group,
    update: (elapsed) => {
      const head = (elapsed * trainSpeed) % 1;
      for (let i = 0; i < cars.length; i++) place(cars[i], head - i * 0.018);
    },
  };
}

