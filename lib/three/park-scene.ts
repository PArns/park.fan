function buildCastle({ track, plain, lit, pbr, loadImage }: BuildCtx): THREE.Group {
  const group = new THREE.Group();

  const wallMat = plain({ ...pbr.stone, color: C.stoneLight, roughness: 0.9, metalness: 0 });
  const wallMat2 = plain({ ...pbr.stone, color: C.stoneBlue, roughness: 0.9, metalness: 0 });
  const goldMat = lit({ color: C.gold, metalness: 0.2, roughness: 0.35 }, 0.5);

  const makeTower = (
    radius: number,
    height: number,
    roofColor: number,
    flagColor: number,
    body: THREE.MeshStandardMaterial
  ) => {
    const tower = new THREE.Group();
    const shaft = new THREE.Mesh(
      track.geo(new THREE.CylinderGeometry(radius, radius * 1.08, height, 20)),
      body
    );
    shaft.position.y = height / 2;
    shaft.castShadow = true;
    shaft.receiveShadow = true;
    tower.add(shaft);

    const roofH = radius * 3.4;
    const roof = new THREE.Mesh(
      track.geo(new THREE.ConeGeometry(radius * 1.25, roofH, 20)),
      lit({ color: roofColor, roughness: 0.55, metalness: 0 }, 0.45)
    );
    roof.position.y = height + roofH / 2;
    roof.castShadow = true;
    tower.add(roof);

    const finial = new THREE.Mesh(
      track.geo(new THREE.SphereGeometry(radius * 0.32, 12, 12)),
      goldMat
    );
    finial.position.y = height + roofH;
    tower.add(finial);

    const poleH = radius * 2.2;
    const pole = new THREE.Mesh(
      track.geo(new THREE.CylinderGeometry(radius * 0.05, radius * 0.05, poleH, 6)),
      goldMat
    );
    pole.position.y = height + roofH + poleH / 2;
    tower.add(pole);
    const flag = new THREE.Mesh(
      track.geo(new THREE.PlaneGeometry(radius * 1.3, radius * 0.7)),
      lit({ color: flagColor, roughness: 0.6, side: THREE.DoubleSide }, 0.5)
    );
    flag.position.set(radius * 0.7, height + roofH + poleH * 0.78, 0);
    tower.add(flag);
    return tower;
  };

  // --- Gatehouse with a central archway TUNNEL on x=0 ---------------------
  // The camera flies straight through, front to back. The clear opening is
  // x∈[-3.5, 3.5], y∈[0, 8], open at both ends (no geometry on the axis).
  const pierGeo = track.geo(new THREE.BoxGeometry(3, 9, 6));
  for (const sx of [-5.5, 5.5]) {
    const pier = new THREE.Mesh(pierGeo, wallMat);
    pier.position.set(sx, 4.5, 0);
    pier.castShadow = true;
    pier.receiveShadow = true;
    group.add(pier);
  }
  // Lintel spanning the piers — its underside (y=8) is the top of the opening,
  // well above the flight height (y≈4.4).
  const lintel = new THREE.Mesh(track.geo(new THREE.BoxGeometry(8.5, 2.6, 6)), wallMat);
  lintel.position.set(0, 9.3, 0);
  lintel.castShadow = true;
  lintel.receiveShadow = true;
  group.add(lintel);
  // Gold arch trim on the front + back faces of the opening.
  const archGeo = track.geo(new THREE.TorusGeometry(3.5, 0.45, 12, 24, Math.PI));
  for (const sz of [3.02, -3.02]) {
    const arch = new THREE.Mesh(archGeo, goldMat);
    arch.position.set(0, 8, sz);
    group.add(arch);
  }

  // Central tower over the tunnel — kept moderate so the castle reads WIDE.
  const central = makeTower(3.1, 11, C.roofBlue, C.flagRed, wallMat);
  central.position.set(0, 10.6, 0);
  group.add(central);

  // A broad spread of towers stepping down and outward (out to x≈±20), plus a
  // back row for depth — a sprawling, imposing footprint rather than one spire.
  const flank: Array<[number, number, number, number, number, THREE.MeshStandardMaterial]> = [
    [-9, 1, 9, C.roofPink, C.flagYellow, wallMat2],
    [9, 1, 9, C.roofPurple, C.flagBlue, wallMat2],
    [-15, 0, 7.5, C.roofTeal, C.flagRed, wallMat],
    [15, 0, 7.5, C.roofPink, C.flagBlue, wallMat],
    [-20.5, -2, 6, C.roofPurple, C.flagYellow, wallMat2],
    [20.5, -2, 6, C.roofTeal, C.flagBlue, wallMat2],
    [-11, -11, 7.5, C.roofBlue, C.flagRed, wallMat],
    [11, -11, 7.5, C.roofPink, C.flagYellow, wallMat],
  ];
  for (const [x, z, h, roof, flag, body] of flank) {
    const tower = makeTower(h >= 9 ? 2.0 : h >= 7 ? 1.6 : 1.3, h, roof, flag, body);
    tower.position.set(x, 0, z);
    group.add(tower);
  }

  // Broad crenellated curtain walls tie the towers into one sprawling mass.
  const makeWall = (x: number, w: number, h: number) => {
    const wall = new THREE.Mesh(track.geo(new THREE.BoxGeometry(w, h, 3)), wallMat);
    wall.position.set(x, h / 2, 0);
    wall.castShadow = true;
    wall.receiveShadow = true;
    group.add(wall);
    const cap = new THREE.Mesh(track.geo(new THREE.BoxGeometry(w, 0.8, 3.4)), wallMat2);
    cap.position.set(x, h + 0.4, 0);
    group.add(cap);
  };
  makeWall(-7, 4, 5);
  makeWall(7, 4, 5);
  makeWall(-12.5, 4.5, 4.5);
  makeWall(12.5, 4.5, 4.5);
  makeWall(-18, 4, 4);
  makeWall(18, 4, 4);

  // park.fan crest high on the central tower, facing the approach (+z). Unlit
  // (basic) material so it stays crisp and legible by day and night.
  const banner = new THREE.Group();
  const frame = new THREE.Mesh(
    track.geo(new THREE.BoxGeometry(6.2, 2.4, 0.3)),
    lit({ color: C.gold, metalness: 0.2, roughness: 0.35 }, 0.5)
  );
  banner.add(frame);
  const board = new THREE.Mesh(
    track.geo(new THREE.BoxGeometry(5.7, 1.9, 0.32)),
    lit({ color: 0x16235c, roughness: 0.5 }, 0.35)
  );
  banner.add(board);
  const logoTex = loadImage('/parkfan-dark.png');
  const logoMat = track.mat(
    new THREE.MeshBasicMaterial({ map: logoTex, transparent: true, toneMapped: false })
  );
  const logo = new THREE.Mesh(track.geo(new THREE.PlaneGeometry(5.2, (5.2 * 219) / 768)), logoMat);
  logo.position.z = 0.2;
  banner.add(logo);
  banner.position.set(0, 15, 3.3); // on the central tower, toward the camera
  group.add(banner);

  return group;
}

