/**
 * park-scene.ts
 * -------------
 * A colorful, Disney-style **amusement park** built in pure three.js, used as
 * the homepage hero background (see `components/layout/hero-three-park.tsx`).
 *
 * The park is laid out as recognizable themed "lands" radiating from a central
 * plaza hub, each with its own ground color, buildings and headline ride:
 *  - **Fantasyland** (back): the fairytale castle, carousel, teacups.
 *  - **Tomorrowland** (right): metallic domes, a rocket ride, the big wheel.
 *  - **Adventureland** (left): a lake, tiki huts and dense palms.
 *  - **Frontierland** (front-right): a western town under the wooden coaster.
 *  - **Main Street** (front): the striped entrance arch and food stalls.
 *
 * The drone-style camera flies a closed loop that swoops in over Main Street
 * and **through the castle gate**, then arcs around the whole park for an
 * overview. Fireworks burst over the castle (vivid at night).
 *
 * Texturing is hybrid: photographic **CC0 PBR maps** (ambientCG, public domain;
 * generated by `scripts/fetch-hero-textures.mjs` into `public/textures/hero/`)
 * for the big surfaces — grass, paving, stone, wood — plus procedural canvas
 * textures for accents (sky, stripes, water ripples).
 *
 * Rendering is deliberately simple and robust for a decorative web background:
 * direct rendering (no post-processing), moderate lighting, low metalness, and
 * **emissive glow** for the night "lights-on" look instead of bloom — so it
 * stays bright and legible on any GPU. Honors `prefers-reduced-motion` with a
 * single static frame. `dispose()` frees all GPU resources + listeners.
 *
 * Imported only by a `ssr:false` dynamic component, so three.js is code-split
 * into its own chunk and never touches SSR or the hero's LCP.
 */

import * as THREE from 'three';

export type SceneTheme = 'light' | 'dark';

export interface ParkSceneHandle {
  resize: (width: number, height: number) => void;
  setTheme: (theme: SceneTheme) => void;
  dispose: () => void;
}

interface CreateOptions {
  theme: SceneTheme;
  /** When true: build the scene, render ONE frame, never start the loop. */
  reducedMotion: boolean;
  /** Called once the scene is built and its textures have loaded (for a loader). */
  onReady?: () => void;
}

interface Animated {
  group: THREE.Object3D;
  update: (elapsed: number, delta: number) => void;
}

