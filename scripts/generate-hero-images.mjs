#!/usr/bin/env node

/**
 * Generate hero-images.ts and hero-images-meta.ts from available park images.
 * Fetches the discovery API at build time to enrich each image with park/attraction metadata.
 * Runs during build: pnpm generate:hero-images
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const PARKS_DIR = path.join(__dirname, '../public/images/parks');
const IMAGES_OUTPUT = path.join(__dirname, '../lib/hero-images.ts');
const META_OUTPUT = path.join(__dirname, '../lib/hero-images-meta.ts');
const API_BASE = 'https://api.park.fan';
const SUPPORTED_EXTENSIONS = ['.jpg', '.jpeg', '.png', '.webp'];

// Themed areas are not tracked in the API — maintain them here.
