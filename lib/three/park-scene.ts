function makeSkyTexture(top: string, bottom: string): THREE.CanvasTexture {
  const canvas = document.createElement('canvas');
  canvas.width = 2;
  canvas.height = 256;
  const ctx = canvas.getContext('2d')!;
  const grad = ctx.createLinearGradient(0, 0, 0, 256);
  grad.addColorStop(0, top);
  grad.addColorStop(1, bottom);
  ctx.fillStyle = grad;
  ctx.fillRect(0, 0, 2, 256);
  const tex = new THREE.CanvasTexture(canvas);
  tex.colorSpace = THREE.SRGBColorSpace;
  return tex;
}

function makeStripeTexture(colorA: string, colorB: string): THREE.CanvasTexture {
  const canvas = document.createElement('canvas');
  canvas.width = 128;
  canvas.height = 16;
  const ctx = canvas.getContext('2d')!;
  const stripes = 8;
  const w = canvas.width / stripes;
  for (let i = 0; i < stripes; i++) {
    ctx.fillStyle = i % 2 === 0 ? colorA : colorB;
    ctx.fillRect(i * w, 0, w + 1, canvas.height);
    const g = ctx.createLinearGradient(i * w, 0, (i + 1) * w, 0);
    g.addColorStop(0, 'rgba(0,0,0,0.16)');
    g.addColorStop(0.5, 'rgba(255,255,255,0.12)');
    g.addColorStop(1, 'rgba(0,0,0,0.16)');
    ctx.fillStyle = g;
    ctx.fillRect(i * w, 0, w + 1, canvas.height);
  }
  const tex = new THREE.CanvasTexture(canvas);
  tex.colorSpace = THREE.SRGBColorSpace;
  tex.wrapS = tex.wrapT = THREE.RepeatWrapping;
  return tex;
}

/**
 * A tileable RollerCoaster-Tycoon-style paved path: a grid of slabs with grout
 * lines and slight per-slab tonal variation + speckle. Reads as classic park
 * pavement at any distance.
 */
function makePathTexture(): THREE.CanvasTexture {
  const size = 256;
  const canvas = document.createElement('canvas');
  canvas.width = canvas.height = size;
  const ctx = canvas.getContext('2d')!;
  ctx.fillStyle = '#766a5b'; // grout
  ctx.fillRect(0, 0, size, size);
  const n = 4;
  const w = size / n;
  const gap = 3;
  const bases = ['#cdbfa6', '#c5b699', '#d4c7af', '#bfb091'];
  for (let gy = 0; gy < n; gy++) {
    for (let gx = 0; gx < n; gx++) {
      ctx.fillStyle = bases[(gx + gy * 2) % bases.length];
      ctx.fillRect(gx * w + gap, gy * w + gap, w - gap * 2, w - gap * 2);
      ctx.fillStyle = 'rgba(0,0,0,0.05)';
      for (let s = 0; s < 7; s++) {
        ctx.fillRect(
          gx * w + gap + Math.random() * (w - 2 * gap),
          gy * w + gap + Math.random() * (w - 2 * gap),
          2,
          2
        );
      }
    }
  }
  const tex = new THREE.CanvasTexture(canvas);
  tex.colorSpace = THREE.SRGBColorSpace;
  tex.wrapS = tex.wrapT = THREE.RepeatWrapping;
  return tex;
}

/** A tileable ripple normal map for the lake, built from summed sine waves. */
function makeWaterNormal(): THREE.CanvasTexture {
  const size = 256;
  const canvas = document.createElement('canvas');
  canvas.width = canvas.height = size;
  const ctx = canvas.getContext('2d')!;
  const img = ctx.createImageData(size, size);
  const TAU = Math.PI * 2;
  const height = (x: number, y: number) => {
    const u = (x / size) * TAU;
    const v = (y / size) * TAU;
    return Math.sin(u * 3 + v) * 0.5 + Math.sin(v * 4 - u * 2) * 0.3 + Math.sin((u + v) * 2) * 0.2;
  };
  for (let y = 0; y < size; y++) {
    for (let x = 0; x < size; x++) {
      const hx = height((x + 1) % size, y) - height((x - 1 + size) % size, y);
      const hy = height(x, (y + 1) % size) - height(x, (y - 1 + size) % size);
      const nx = -hx * 0.5;
      const ny = -hy * 0.5;
      const len = Math.hypot(nx, ny, 1);
      const idx = (y * size + x) * 4;
      img.data[idx] = ((nx / len) * 0.5 + 0.5) * 255;
      img.data[idx + 1] = ((ny / len) * 0.5 + 0.5) * 255;
      img.data[idx + 2] = (1 / len) * 0.5 * 255 + 128;
      img.data[idx + 3] = 255;
    }
  }
  ctx.putImageData(img, 0, 0);
  const tex = new THREE.CanvasTexture(canvas);
  tex.wrapS = tex.wrapT = THREE.RepeatWrapping;
  return tex;
}

/** Vertical streaks for a scrolling waterfall sheet. */
function makeWaterfallTexture(): THREE.CanvasTexture {
  const w = 32;
  const h = 128;
  const canvas = document.createElement('canvas');
  canvas.width = w;
  canvas.height = h;
  const ctx = canvas.getContext('2d')!;
  ctx.fillStyle = '#cfeefe';
  ctx.fillRect(0, 0, w, h);
  for (let x = 0; x < w; x++) {
    const shade = 200 + Math.floor(Math.random() * 55);
    ctx.fillStyle = `rgb(${shade},${shade + 5},255)`;
    ctx.fillRect(x, 0, 1, h);
  }
  const tex = new THREE.CanvasTexture(canvas);
  tex.colorSpace = THREE.SRGBColorSpace;
  tex.wrapS = tex.wrapT = THREE.RepeatWrapping;
  return tex;
}

// ---------------------------------------------------------------------------
// Resource tracking
// ---------------------------------------------------------------------------

class Tracker {
  readonly geometries = new Set<THREE.BufferGeometry>();
  readonly materials = new Set<THREE.Material>();
  readonly textures = new Set<THREE.Texture>();

  geo<T extends THREE.BufferGeometry>(g: T): T {
    this.geometries.add(g);
    return g;
  }
  mat<T extends THREE.Material>(m: T): T {
    this.materials.add(m);
    return m;
  }
  tex<T extends THREE.Texture>(t: T): T {
    this.textures.add(t);
    return t;
  }
  disposeAll() {
    this.geometries.forEach((g) => g.dispose());
    this.materials.forEach((m) => m.dispose());
    this.textures.forEach((t) => t.dispose());
    this.geometries.clear();
    this.materials.clear();
    this.textures.clear();
  }
}

type MatFactory = (params: THREE.MeshStandardMaterialParameters) => THREE.MeshStandardMaterial;
type LitFactory = (
  params: THREE.MeshStandardMaterialParameters,
  glow?: number
) => THREE.MeshStandardMaterial;

interface PbrMaps {
  map: THREE.Texture;
  normalMap: THREE.Texture;
  roughnessMap: THREE.Texture;
}

interface BuildCtx {
  track: Tracker;
  plain: MatFactory;
  lit: LitFactory;
  pbr: Record<'grass' | 'paving' | 'stone' | 'wood', PbrMaps>;
  lampLights: THREE.PointLight[];
  registerWater: (m: THREE.MeshStandardMaterial) => void;
  /** Load a single sRGB image texture (e.g. the park.fan logo). */
  loadImage: (url: string) => THREE.Texture;
  /** Max anisotropy of the renderer (for crisp ground textures at grazing angles). */
  maxAniso: number;
}

