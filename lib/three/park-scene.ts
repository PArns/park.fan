export function createParkScene(canvas: HTMLCanvasElement, opts: CreateOptions): ParkSceneHandle {
  const track = new Tracker();
  const palette: Palette = DAY; // theme-independent: always the bright daytime look

  const renderer = new THREE.WebGLRenderer({
    canvas,
    antialias: true,
    alpha: false,
    powerPreference: 'high-performance',
  });
  const initialW = canvas.clientWidth || 1280;
  const initialH = canvas.clientHeight || 600;
  const dpr = Math.min(window.devicePixelRatio || 1, 2);
  const maxAniso = renderer.capabilities.getMaxAnisotropy();
  renderer.setPixelRatio(dpr);
  renderer.setSize(initialW, initialH, false);
  renderer.shadowMap.enabled = true;
  renderer.shadowMap.type = THREE.PCFSoftShadowMap;
  renderer.toneMapping = THREE.ACESFilmicToneMapping;
  renderer.toneMappingExposure = 1.0;

  const scene = new THREE.Scene();
  const skyTex = track.tex(makeSkyTexture(palette.skyTop, palette.skyBottom));
  scene.background = skyTex;
  scene.fog = new THREE.Fog(palette.fog, palette.fogNear, palette.fogFar);

  const camera = new THREE.PerspectiveCamera(52, initialW / initialH, 0.1, 500);
  camera.position.set(0, 6, 30);

  // -- Flying camera (cinematic): glide in low over Main Street, dead-straight
  // THROUGH the freestanding gate, THROUGH the castle's archway tunnel, and out
  // into the hidden courtyard garden behind it — then rise and arc wide around
  // the whole park past the mountains. Points 1–6 stay on x=0 so the whole
  // gate→tunnel→reveal run is a straight line. The look target reads a little
  // further along the same path, so the camera always faces where it's going.
  const flightPath = new THREE.CatmullRomCurve3(
    [
      new THREE.Vector3(0, 13, 42), // approach overview
      new THREE.Vector3(0, 6.5, 14), // descend toward the gate
      new THREE.Vector3(0, 4.5, -13), // brief dramatic dip THROUGH the gate
      new THREE.Vector3(0, 4.6, -30), // through the castle archway tunnel (x=0)
      new THREE.Vector3(0, 8, -44), // emerge into the courtyard, climbing
      new THREE.Vector3(0, 18, -56), // frame the finale lake + the mountain marker
      new THREE.Vector3(-36, 19, -46), // …then a closer overview orbit so the park,
      new THREE.Vector3(-52, 16, -4), //   its attractions and the wide castle stay
      new THREE.Vector3(-38, 14, 42), //   present and full in frame (not distant)
      new THREE.Vector3(0, 16, 54),
      new THREE.Vector3(38, 14, 42),
      new THREE.Vector3(52, 16, -4),
      new THREE.Vector3(36, 19, -46),
    ],
    true,
    'catmullrom',
    0.5
  );
  const LAP_SECONDS = 96; // slow, stately flight
  const LOOK_AHEAD = 0.016;
  const camPos = new THREE.Vector3();
  const camTarget = new THREE.Vector3();

  // -- Lights (no IBL; moderate so nothing washes out) --------------------
  const hemi = new THREE.HemisphereLight(
    palette.hemiSky,
    palette.hemiGround,
    palette.hemiIntensity
  );
  scene.add(hemi);
  const ambient = new THREE.AmbientLight(palette.ambient, palette.ambientIntensity);
  scene.add(ambient);
  const sun = new THREE.DirectionalLight(palette.sun, palette.sunIntensity);
  sun.position.set(-34, 48, 28);
  sun.castShadow = true;
  const shadowSize = initialW < 768 ? 1024 : 2048;
  sun.shadow.mapSize.set(shadowSize, shadowSize);
  sun.shadow.camera.near = 1;
  sun.shadow.camera.far = 160;
  sun.shadow.camera.left = -64;
  sun.shadow.camera.right = 64;
  sun.shadow.camera.top = 64;
  sun.shadow.camera.bottom = -54;
  sun.shadow.bias = -0.0004;
  sun.shadow.normalBias = 0.02;
  scene.add(sun);

  // -- Material factories. flatShading gives the faceted, toy-like "toon" look
  // the park is going for (stylized & playful), and a constant emissive glow
  // keeps lights/accents lively. Callers can still override flatShading.
  const emissiveMats: THREE.MeshStandardMaterial[] = [];
  const plainMaterial: MatFactory = (params) =>
    track.mat(new THREE.MeshStandardMaterial({ flatShading: true, ...params }));
  const litMaterial: LitFactory = (params, glow = 0.9) => {
    const m = track.mat(new THREE.MeshStandardMaterial({ flatShading: true, ...params }));
    m.emissive = new THREE.Color((params.color as THREE.ColorRepresentation) ?? 0xffffff);
    m.userData.glow = glow;
    m.emissiveIntensity = palette.emissive * glow;
    emissiveMats.push(m);
    return m;
  };

  // -- Textures (async; the scene renders immediately, onReady fires on load) --
  const manager = new THREE.LoadingManager();
  let readyFired = false;
  const fireReady = () => {
    if (readyFired) return;
    readyFired = true;
    opts.onReady?.();
  };
  manager.onLoad = fireReady;
  const texLoader = new THREE.TextureLoader(manager);
  const loadTex = (url: string, srgb: boolean, repeat: number) => {
    const t = texLoader.load(url);
    t.wrapS = t.wrapT = THREE.RepeatWrapping;
    t.repeat.set(repeat, repeat);
    t.anisotropy = maxAniso;
    t.colorSpace = srgb ? THREE.SRGBColorSpace : THREE.NoColorSpace;
    return track.tex(t);
  };
  const loadPbr = (name: string, repeat: number): PbrMaps => ({
    map: loadTex(`/textures/hero/${name}/color.webp`, true, repeat),
    normalMap: loadTex(`/textures/hero/${name}/normal.webp`, false, repeat),
    roughnessMap: loadTex(`/textures/hero/${name}/roughness.webp`, false, repeat),
  });
  const pbr: BuildCtx['pbr'] = {
    grass: loadPbr('grass', 30),
    paving: loadPbr('paving', 9),
    stone: loadPbr('stone', 3),
    wood: loadPbr('wood', 2),
  };
  const loadImage = (url: string) => {
    const t = texLoader.load(url);
    t.colorSpace = THREE.SRGBColorSpace;
    t.anisotropy = maxAniso;
    return track.tex(t);
  };

  // -- Build the world ----------------------------------------------------
  const lampLights: THREE.PointLight[] = [];
  const waterMats: THREE.MeshStandardMaterial[] = [];
  const ctx: BuildCtx = {
    track,
    plain: plainMaterial,
    lit: litMaterial,
    pbr,
    lampLights,
    registerWater: (m) => waterMats.push(m),
    loadImage,
    maxAniso,
  };

  const animated: Animated[] = [];
  const root = new THREE.Group();
  scene.add(root);

  root.add(buildGround(ctx));
  root.add(buildMountains(ctx)); // distant horizon backdrop for depth + scale

  // Fantasyland (back center) — the camera flies through the freestanding gate,
  // through the castle's archway tunnel, and into the hidden courtyard behind.
  const castle = buildCastle(ctx);
  castle.position.set(0, 0, -30);
  root.add(castle);
  root.add(buildCastleGate(ctx)); // freestanding flythrough arch at z≈-13
  root.add(buildCastleGarden(ctx)); // hidden courtyard garden right behind the castle

  // The bombastic reveal further back: a great lake with a dancing fountain
  // show, a waterfall mountain, and a giant glowing park.fan marker.
  const finale = buildGrandFinale(ctx);
  root.add(finale.group);
  animated.push(finale);

  // A whole second park sprawls BEHIND the castle, around the lake: a second
  // Ferris wheel, a drop-tower "Startflyer", an observation tower, a second
  // coaster (a different, bluer style), and rows of food/shop stalls.
  const backFerris = buildFerrisWheel(ctx);
  backFerris.group.position.set(-34, 0, -60);
  backFerris.group.rotation.y = 0.5;
  root.add(backFerris.group);
  animated.push(backFerris);

  const dropTower = buildDropTower(ctx);
  dropTower.group.position.set(34, 0, -58);
  root.add(dropTower.group);
  animated.push(dropTower);

  const obsTower = buildObservationTower(ctx);
  obsTower.group.position.set(-40, 0, -80);
  root.add(obsTower.group);
  animated.push(obsTower);

  // Second coaster — a compact, swooping steel circuit in a different (teal) style.
  const coaster2 = buildCoaster(ctx, {
    color: 0x33d6c0,
    speed: 0.07,
    pts: [
      new THREE.Vector3(20, 1, -52),
      new THREE.Vector3(34, 2, -62),
      new THREE.Vector3(40, 10, -74),
      new THREE.Vector3(30, 15, -84),
      new THREE.Vector3(14, 11, -82),
      new THREE.Vector3(10, 5, -70),
      new THREE.Vector3(18, 3, -60),
    ],
  });
  root.add(coaster2.group);
  animated.push(coaster2);

  // Rows of stalls around the back park (RCT-style abundance of stands).
  const backStalls: Array<[number, number, number, number]> = [
    [-14, -50, 0.2, 0xff5d6c],
    [-6, -50, -0.2, 0x5db8ff],
    [6, -50, 0.2, 0x8be04e],
    [14, -50, -0.2, 0xffd166],
    [-20, -54, 0.6, 0xff9f43],
    [20, -54, -0.6, 0xb57bff],
    [-24, -70, 0.8, 0x5db8ff],
    [24, -70, -0.8, 0xff5d6c],
  ];
  for (const [x, z, r, color] of backStalls) {
    const stall = buildStall(ctx, color);
    stall.position.set(x, 0, z);
    stall.rotation.y = r;
    root.add(stall);
  }

  const carousel = buildCarousel(ctx);
  carousel.group.position.set(-11, 0, -19);
  root.add(carousel.group);
  animated.push(carousel);

  const teacups = buildTeacups(ctx);
  teacups.group.position.set(11, 0, -19);
  root.add(teacups.group);
  animated.push(teacups);

  // Tomorrowland (right)
  const ferris = buildFerrisWheel(ctx);
  ferris.group.position.set(27, 0, -12);
  root.add(ferris.group);
  animated.push(ferris);

  const rocket = buildRocketRide(ctx);
  rocket.group.position.set(22, 0, 7);
  root.add(rocket.group);
  animated.push(rocket);

  buildDomes(ctx).forEach((d, i) => {
    d.position.set(32 + (i % 2) * 4, 0, -2 + i * 6);
    root.add(d);
  });

  // Adventureland (left): lake + tiki huts + palms
  const lake = buildLake(ctx);
  lake.group.position.set(-26, 0, 6);
  root.add(lake.group);
  animated.push(lake);

  const tikiSpots: Array<[number, number, number]> = [
    [-30, -7, 0.4],
    [-21, 13, -0.5],
    [-34, 4, 0.2],
  ];
  for (const [x, z, r] of tikiSpots) {
    const hut = buildTikiHut(ctx);
    hut.position.set(x, 0, z);
    hut.rotation.y = r;
    root.add(hut);
  }

  // Frontierland (front-right): western town
  const westernSpots: Array<[number, number, number, number]> = [
    [17, 19, 0.2, 0xc25b4a],
    [23, 14, -0.5, 0x9c6b3f],
    [13, 13, 0.5, 0xb98a52],
  ];
  for (const [x, z, r, color] of westernSpots) {
    const b = buildWesternBuilding(ctx, color);
    b.position.set(x, 0, z);
    b.rotation.y = r;
    root.add(b);
  }

  // Swing ride near the hub
  const swing = buildSwingRide(ctx);
  swing.group.position.set(-15, 0, -4);
  root.add(swing.group);
  animated.push(swing);

  // The coaster encircles the whole park (Frontierland-style wooden supports).
  const coaster = buildCoaster(ctx);
  root.add(coaster.group);
  animated.push(coaster);

  // Main Street (front): entrance arch + stalls + tents
  root.add(buildEntranceGate(ctx));
  // Plenty of stalls lining the main avenue + plaza edges (RCT-style abundance).
  const stallSpots: Array<[number, number, number, number]> = [
    [-6, 14, 0.3, 0xff5d6c],
    [6, 14, -0.3, 0x5db8ff],
    [-12, 20, 0.5, 0x8be04e],
    [12, 20, -0.5, 0xffd166],
    [-9, 26, 0.3, 0xff9f43],
    [9, 26, -0.3, 0xb57bff],
    [-16, 30, 0.6, 0x5db8ff],
    [16, 30, -0.6, 0xff5d6c],
    [-14, 9, 1.4, 0xffd166],
    [14, 9, -1.4, 0x8be04e],
    [-18, 15, 0.9, 0xb57bff],
    [18, 15, -0.9, 0xff9f43],
  ];
  for (const [x, z, r, color] of stallSpots) {
    const stall = buildStall(ctx, color);
    stall.position.set(x, 0, z);
    stall.rotation.y = r;
    root.add(stall);
  }
  const tentSpots: Array<[number, number, string, string]> = [
    [-9, 8, '#ff5d6c', '#fff3f7'],
    [9, 8, '#5db8ff', '#fff3f7'],
  ];
  for (const [x, z, a, b] of tentSpots) {
    const tent = buildTent(ctx, a, b);
    tent.position.set(x, 0, z);
    root.add(tent);
  }

  // Lanterns along the central paths (alternate ones cast a night point-light).
  const lanternSpots: Array<[number, number]> = [
    [3.5, 9],
    [-3.5, 9],
    [7, 3],
    [-7, 3],
    [4, -5],
    [-4, -5],
    [11, 1],
    [-11, 1],
  ];
  lanternSpots.forEach(([x, z], i) => root.add(buildLantern(ctx, x, z, i % 2 === 0)));

  // Trees framing the park + filling the empty stretches (front avenue, edges,
  // and all around the finale behind the castle) so no area reads as bare.
  const treeSpots: Array<[number, number]> = [
    // outer ring / edges
    [-40, 14],
    [-44, -18],
    [40, 16],
    [45, -4],
    [44, -24],
    [-46, -4],
    // front avenue (Main Street) — denser
    [-18, 24],
    [8, 24],
    [20, 26],
    [-26, 26],
    [-14, 30],
    [14, 30],
    [-22, 33],
    [22, 33],
    [-9, 27],
    [9, 27],
    // mid-park fill
    [34, 22],
    [-36, 22],
    [30, 4],
    [-31, -3],
    [33, -16],
    [-33, -14],
    // around the finale behind the castle (z further negative)
    [-22, -50],
    [22, -50],
    [-30, -60],
    [30, -60],
    [-16, -64],
    [16, -64],
    [-34, -44],
    [34, -44],
    [-12, -74],
    [12, -74],
  ];
  for (const [x, z] of treeSpots) {
    const tree = buildTree(ctx);
    tree.scale.setScalar(0.9 + ((x * 7 + z * 13) % 5) / 8);
    tree.position.set(x, 0, z);
    tree.rotation.y = (x + z) % 6;
    root.add(tree);
  }

  root.add(buildFences(ctx));
  root.add(buildFlowerBeds(ctx));
  root.add(buildStreetProps(ctx)); // RCT details: benches, bins, signs, bushes
  root.add(buildBunting(ctx)); // colorful pennant strings over the plaza

  const visitors = buildVisitors(ctx);
  root.add(visitors.group);
  animated.push(visitors);

  const clouds = buildClouds(ctx);
  root.add(clouds.group);
  animated.push(clouds);

  // Fireworks burst OVER THE CASTLE only.
  const fireworks = buildFireworks(ctx, new THREE.Vector3(0, 22, -30));
  root.add(fireworks.group);
  animated.push(fireworks);

  const stars = buildStars(track);
  scene.add(stars);

  // -- Theme application --------------------------------------------------
  // One-time look setup (theme-independent — always the bright daytime look).
  // Sky / fog / lights are already configured from DAY at init; here we apply the
  // constant glow to the emissive surfaces built above and tint the lake water.
  for (const m of emissiveMats) m.emissiveIntensity = palette.emissive * (m.userData.glow ?? 0.9);
  for (const w of waterMats) w.color.set(palette.water);
  for (const l of lampLights) l.intensity = palette.lampIntensity;
  stars.visible = false;

  // -- Render loop --------------------------------------------------------
  const clock = new THREE.Clock();
  let frameId = 0;
  let running = false;

  const renderFrame = (elapsed: number, delta: number) => {
    for (const a of animated) a.update(elapsed, delta);
    const t = (elapsed / LAP_SECONDS) % 1;
    flightPath.getPointAt(t, camPos);
    flightPath.getPointAt((t + LOOK_AHEAD) % 1, camTarget);
    camera.position.copy(camPos);
    camera.lookAt(camTarget);
    renderer.render(scene, camera);
  };

  const loop = () => {
    if (!running) return;
    frameId = requestAnimationFrame(loop);
    const delta = Math.min(clock.getDelta(), 0.05);
    renderFrame(clock.elapsedTime, delta);
  };
  const start = () => {
    if (running) return;
    running = true;
    clock.start();
    loop();
  };
  const stop = () => {
    running = false;
    if (frameId) cancelAnimationFrame(frameId);
    frameId = 0;
  };

  const onVisibility = () => {
    if (document.hidden) stop();
    else if (!opts.reducedMotion) start();
  };
  document.addEventListener('visibilitychange', onVisibility);

  if (opts.reducedMotion) {
    renderFrame(0, 0);
    fireReady();
  } else {
    start();
  }

  return {
    resize(width, height) {
      camera.aspect = width / height;
      camera.updateProjectionMatrix();
      renderer.setSize(width, height, false);
      if (opts.reducedMotion) renderFrame(clock.elapsedTime, 0);
    },
    setTheme() {
      // The park is intentionally theme-independent (always the bright daytime
      // look), so nothing changes when the site toggles light/dark.
    },
    dispose() {
      stop();
      document.removeEventListener('visibilitychange', onVisibility);
      scene.traverse((o) => {
        const im = o as THREE.InstancedMesh;
        if (im.isInstancedMesh) im.dispose();
      });
      track.disposeAll();
      renderer.dispose();
    },
  };
}

