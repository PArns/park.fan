// --- filesystem helpers ---

function getAllImages() {
  if (!fs.existsSync(PARKS_DIR)) {
    console.error('❌ Parks directory not found:', PARKS_DIR);
    process.exit(1);
  }

  const images = [];
  const parkDirs = fs
    .readdirSync(PARKS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name);

  for (const parkDir of parkDirs) {
    const parkPath = path.join(PARKS_DIR, parkDir);

    for (const file of fs.readdirSync(parkPath, { withFileTypes: true })) {
      if (!file.isFile()) continue;
      const ext = path.extname(file.name).toLowerCase();
      const base = path.basename(file.name, ext);
      if (SUPPORTED_EXTENSIONS.includes(ext) && base !== 'background') {
        images.push(`/images/parks/${parkDir}/${file.name}`);
      }
    }
  }

  return images.sort();
}

function groupByPark(images) {
  const byPark = {};
  for (const p of images) {
    const parkSlug = p.split('/')[3];
    (byPark[parkSlug] ??= []).push(p);
  }
  return byPark;
}

// --- API helpers ---

async function fetchJson(url) {
  const res = await fetch(url, { headers: { 'User-Agent': 'park.fan-build/1.0' } });
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return res.json();
}

async function buildGeoMap() {
  const geo = await fetchJson(`${API_BASE}/v1/discovery/geo`);
  const map = {};
  for (const continent of geo.continents ?? []) {
    for (const country of continent.countries ?? []) {
      for (const city of country.cities ?? []) {
        for (const park of city.parks ?? []) {
          // URL shape: /v1/parks/{continent}/{countrySlug}/{city}/{parkSlug}
          const urlParts = park.url.split('/');
          const countrySlug = urlParts[4];
          map[park.slug] = {
            name: park.name,
            city: city.name,
            countrySlug,
            url: park.url,
          };
        }
      }
    }
  }
  return map;
}

async function fetchAttractionNames(parkUrl) {
  try {
    const park = await fetchJson(`${API_BASE}${parkUrl}`);
    const bySlug = {};
    for (const a of park.attractions ?? []) {
      bySlug[a.slug] = a.name;
    }
    return bySlug;
  } catch (e) {
    console.warn(`  ⚠️  Could not fetch attractions for ${parkUrl}: ${e.message}`);
    return {};
  }
}

// --- code generation ---

function generateImagesFile(images) {
  return `// Auto-generated by scripts/generate-hero-images.mjs
// DO NOT EDIT MANUALLY - Run 'pnpm build' or 'pnpm generate:hero-images' to regenerate

/**
 * Available hero background images for the homepage
 * Generated at build time from public/images/parks/[park]/background.jpg and attraction images
 */
export const HERO_IMAGES = [
${images.map((img) => `  '${img}',`).join('\n')}
] as const;

export type HeroImage = typeof HERO_IMAGES[number];
`;
}

function serializeValue(v) {
  if (v === undefined) return undefined;
  return `'${v.replace(/'/g, "\\'")}'`;
}

function generateMetaFile(meta) {
  const entries = Object.entries(meta)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([imgPath, m]) => {
      const lines = [
        `    parkName: ${serializeValue(m.parkName)},`,
        `    city: ${serializeValue(m.city)},`,
        `    countrySlug: ${serializeValue(m.countrySlug)},`,
        m.attractionName ? `    attractionName: ${serializeValue(m.attractionName)},` : null,
        m.area ? `    area: ${serializeValue(m.area)},` : null,
      ].filter(Boolean);
      return `  '${imgPath}': {\n${lines.join('\n')}\n  },`;
    })
    .join('\n');

  return `// Auto-generated by scripts/generate-hero-images.mjs
// DO NOT EDIT MANUALLY - Run 'pnpm build' or 'pnpm generate:hero-images' to regenerate

export interface HeroImageMeta {
  parkName: string;
  city: string;
  /** Matches the 'geo.countries.*' translation key */
  countrySlug: string;
  attractionName?: string;
  /** Themed area within the park — maintained manually in AREA_OVERRIDES (not in API) */
  area?: string;
}

export const HERO_IMAGE_META: Record<string, HeroImageMeta> = {
${entries}
};
`;
}

// --- main ---

async function main() {
  console.log('🎨 Generating hero images...');
  const images = getAllImages();
  console.log(`   Found ${images.length} images`);

  console.log('🌐 Fetching geo structure from API...');
  let geoMap = {};
  try {
    geoMap = await buildGeoMap();
    console.log(`   Mapped ${Object.keys(geoMap).length} parks`);
  } catch (e) {
    console.warn(`⚠️  Could not fetch geo structure: ${e.message}`);
    console.warn('   Metadata will be sparse (park names/cities unavailable)');
  }

  const byPark = groupByPark(images);
  const meta = {};

  for (const [parkSlug, parkImages] of Object.entries(byPark)) {
    const parkInfo = geoMap[parkSlug];
    if (!parkInfo) {
      console.warn(`   ⚠️  ${parkSlug}: not found in geo structure, skipping metadata`);
      continue;
    }

    console.log(`   Fetching attractions for ${parkSlug}...`);
    const attractionsBySlug = await fetchAttractionNames(parkInfo.url);

    for (const imagePath of parkImages) {
      const filename = imagePath.split('/').pop();
      const ext = path.extname(filename);
      const attractionSlug = path.basename(filename, ext);
      const isBackground = attractionSlug === 'background';

      const attractionName = isBackground ? undefined : attractionsBySlug[attractionSlug];
      const area = isBackground ? undefined : AREA_OVERRIDES[`${parkSlug}/${attractionSlug}`];

      meta[imagePath] = {
        parkName: parkInfo.name,
        city: parkInfo.city,
        countrySlug: parkInfo.countrySlug,
        ...(attractionName !== undefined && { attractionName }),
        ...(area !== undefined && { area }),
      };
    }
  }

  const libDir = path.dirname(IMAGES_OUTPUT);
  if (!fs.existsSync(libDir)) fs.mkdirSync(libDir, { recursive: true });

  fs.writeFileSync(IMAGES_OUTPUT, generateImagesFile(images), 'utf8');
  console.log(`✅ Written ${IMAGES_OUTPUT}`);

  fs.writeFileSync(META_OUTPUT, generateMetaFile(meta), 'utf8');
  console.log(`✅ Written ${META_OUTPUT}`);
}

main().catch((e) => {
  console.error('❌ Fatal:', e);
  process.exit(1);
});
